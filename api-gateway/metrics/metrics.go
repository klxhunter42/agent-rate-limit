const namespace = "api_gateway"

// Metrics holds all Prometheus instruments for the API gateway.
type Metrics struct {
	RequestLatency    *prometheus.HistogramVec
	QueueDepth        prometheus.GaugeFunc
	ErrorRate         *prometheus.CounterVec
	RateLimitHits     *prometheus.CounterVec
	ActiveConnections prometheus.Gauge
	TokenInput        *prometheus.CounterVec
	TokenOutput       *prometheus.CounterVec
	UpstreamRetries   prometheus.Counter
	Upstream429       prometheus.Counter
	AdaptiveLimit     *prometheus.GaugeVec
	AdaptiveInFlight  *prometheus.GaugeVec
	CostTotal         *prometheus.CounterVec
	ModelFallback     *prometheus.CounterVec
	TTFB              *prometheus.HistogramVec
	registry          *prometheus.Registry
	queueDepthFn      func() float64
	pricing           map[string]modelPrice
	usageRecorder     func(model string, input, output int, cost float64)
}

type modelPrice struct {
	inputPerMillion  float64
	outputPerMillion float64
}

// New creates and registers all Prometheus metrics. The queueDepthFn callback
// is invoked on every scrape to report the current queue length.
func New(queueDepthFn func() float64, pricing map[string][2]float64) *Metrics {
	registry := prometheus.NewRegistry()

	// Convert pricing map.
	pm := make(map[string]modelPrice, len(pricing))
	for model, prices := range pricing {
		pm[model] = modelPrice{inputPerMillion: prices[0], outputPerMillion: prices[1]}
	}

	m := &Metrics{
		registry:     registry,
		queueDepthFn: queueDepthFn,
		pricing:      pm,

		RequestLatency: prometheus.NewHistogramVec(prometheus.HistogramOpts{
			Namespace: namespace,
			Name:      "request_latency_seconds",
			Help:      "Latency of HTTP requests in seconds.",
			Buckets:   []float64{0.05, 0.1, 0.25, 0.5, 1, 2.5, 5, 10, 30, 60},
		}, []string{"method", "path", "status"}),

		ErrorRate: prometheus.NewCounterVec(prometheus.CounterOpts{
			Namespace: namespace,
			Name:      "error_total",
			Help:      "Total number of errors by type.",
		}, []string{"type"}),

		RateLimitHits: prometheus.NewCounterVec(prometheus.CounterOpts{
			Namespace: namespace,
			Name:      "rate_limit_hits_total",
			Help:      "Total number of rate-limited requests.",
		}, []string{"key"}),

		ActiveConnections: prometheus.NewGauge(prometheus.GaugeOpts{
			Namespace: namespace,
			Name:      "active_connections",
			Help:      "Current number of active connections.",
		}),

		TokenInput: prometheus.NewCounterVec(prometheus.CounterOpts{
			Namespace: namespace,
			Name:      "token_input_total",
			Help:      "Total input tokens consumed by model.",
		}, []string{"model"}),

		TokenOutput: prometheus.NewCounterVec(prometheus.CounterOpts{
			Namespace: namespace,
			Name:      "token_output_total",
			Help:      "Total output tokens generated by model.",
		}, []string{"model"}),

		UpstreamRetries: prometheus.NewCounter(prometheus.CounterOpts{
			Namespace: namespace,
			Name:      "upstream_retries_total",
			Help:      "Total number of upstream retries on 429.",
		}),

		Upstream429: prometheus.NewCounter(prometheus.CounterOpts{
			Namespace: namespace,
			Name:      "upstream_429_total",
			Help:      "Total number of 429 responses received from upstream.",
		}),

		AdaptiveLimit: prometheus.NewGaugeVec(prometheus.GaugeOpts{
			Namespace: namespace,
			Name:      "adaptive_limit",
			Help:      "Current adaptive concurrency limit per model.",
		}, []string{"model"}),

		AdaptiveInFlight: prometheus.NewGaugeVec(prometheus.GaugeOpts{
			Namespace: namespace,
			Name:      "adaptive_in_flight",
			Help:      "Current in-flight requests per model.",
		}, []string{"model"}),

		CostTotal: prometheus.NewCounterVec(prometheus.CounterOpts{
			Namespace: namespace,
			Name:      "cost_total",
			Help:      "Estimated cost in USD by model, calculated from token usage * configured pricing.",
		}, []string{"model"}),

		ModelFallback: prometheus.NewCounterVec(prometheus.CounterOpts{
			Namespace: namespace,
			Name:      "model_fallback_total",
			Help:      "Number of times a request was routed to a different model.",
		}, []string{"requested", "selected"}),

		TTFB: prometheus.NewHistogramVec(prometheus.HistogramOpts{
			Namespace: namespace,
			Name:      "ttfb_seconds",
			Help:      "Time to first byte for streaming responses.",
			Buckets:   []float64{0.01, 0.025, 0.05, 0.1, 0.25, 0.5, 1, 2.5, 5},
		}, []string{"model"}),
	}

	m.QueueDepth = prometheus.NewGaugeFunc(prometheus.GaugeOpts{
		Namespace: namespace,
		Name:      "queue_depth",
		Help:      "Current depth of the AI job queue.",
	}, queueDepthFn)

	registry.MustRegister(
		m.RequestLatency,
		m.QueueDepth,
		m.ErrorRate,
		m.RateLimitHits,
		m.ActiveConnections,
		m.TokenInput,
		m.TokenOutput,
		m.UpstreamRetries,
		m.Upstream429,
		m.AdaptiveLimit,
		m.AdaptiveInFlight,
		m.CostTotal,
		m.ModelFallback,
		m.TTFB,
	)

	return m
}

// Handler returns an HTTP handler that serves Prometheus metrics.
func (m *Metrics) Handler() http.Handler {
	return promhttp.HandlerFor(m.registry, promhttp.HandlerOpts{
		ErrorLog: log.New(io.Discard, "", 0),
	})
}

// statusWriter wraps http.ResponseWriter to capture the actual status code.
type statusWriter struct {
	http.ResponseWriter
	status int
}

func newStatusWriter(w http.ResponseWriter) *statusWriter {
	return &statusWriter{ResponseWriter: w, status: http.StatusOK}
}

func (sw *statusWriter) WriteHeader(code int) {
	sw.status = code
	sw.ResponseWriter.WriteHeader(code)
}

// Middleware instruments every request with latency and active-connection tracking.
func (m *Metrics) Middleware(next http.Handler) http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		m.ActiveConnections.Inc()
		defer m.ActiveConnections.Dec()

		sw := newStatusWriter(w)
		start := time.Now()

		next.ServeHTTP(sw, r)

		routePattern := chi.RouteContext(r.Context()).RoutePattern()
		if routePattern == "" || routePattern == "/*" {
			// Avoid unbounded cardinality from raw URL paths.
			routePattern = "_unknown"
		}

		status := strconv.Itoa(sw.status)
		duration := time.Since(start).Seconds()
		m.RequestLatency.WithLabelValues(r.Method, routePattern, status).Observe(duration)
	})
}

// IncError increments the error counter for the given error type.
func (m *Metrics) IncError(errType string) {
	m.ErrorRate.WithLabelValues(errType).Inc()
}

// IncRateLimit increments the rate-limit counter for the given key.
// Agent-specific keys are hashed to prevent unbounded cardinality.
func (m *Metrics) IncRateLimit(key string) {
	if strings.HasPrefix(key, "agent:") {
		h := sha1.Sum([]byte(key))
		key = "agent:" + hex.EncodeToString(h[:])[:8]
	}
	m.RateLimitHits.WithLabelValues(key).Inc()
}

// RecordTokens records input and output token usage for a model and accumulates cost.
func (m *Metrics) RecordTokens(model string, input, output int) {
	if input > 0 {
		m.TokenInput.WithLabelValues(model).Add(float64(input))
	}
	if output > 0 {
		m.TokenOutput.WithLabelValues(model).Add(float64(output))
	}
	var cost float64
	if p, ok := m.pricing[model]; ok {
		cost = float64(input)/1_000_000*p.inputPerMillion + float64(output)/1_000_000*p.outputPerMillion
		if cost > 0 {
			m.CostTotal.WithLabelValues(model).Add(cost)
		}
	}
	if m.usageRecorder != nil && (input > 0 || output > 0) {
		m.usageRecorder(model, input, output, cost)
	}
}

// SetUsageRecorder registers a callback invoked after every RecordTokens call.
func (m *Metrics) SetUsageRecorder(fn func(model string, input, output int, cost float64)) {
	m.usageRecorder = fn
}

// IncRetry increments the upstream retry counter.
func (m *Metrics) IncRetry() {
	m.UpstreamRetries.Inc()
}

// Inc429 increments the upstream 429 counter.
func (m *Metrics) Inc429() {
	m.Upstream429.Inc()
}

// RecordFallback records a model fallback event.
func (m *Metrics) RecordFallback(requested, selected string) {
	m.ModelFallback.WithLabelValues(requested, selected).Inc()
}

// RecordTTFB records time-to-first-byte for a streaming response.
func (m *Metrics) RecordTTFB(model string, d time.Duration) {
	m.TTFB.WithLabelValues(model).Observe(d.Seconds())
}

// ModelStatusSnapshot holds per-model limiter state for metrics export.
type ModelStatusSnapshot struct {
	Name     string
	Limit    float64
	InFlight float64
}

// Registry returns the underlying Prometheus registry for external metric registration.
func (m *Metrics) Registry() *prometheus.Registry {
	return m.registry
}

// UpdateAdaptiveMetrics sets the adaptive limit and in-flight gauges from a limiter snapshot.
func (m *Metrics) UpdateAdaptiveMetrics(statuses []ModelStatusSnapshot) {
	for _, s := range statuses {
		m.AdaptiveLimit.WithLabelValues(s.Name).Set(s.Limit)
		m.AdaptiveInFlight.WithLabelValues(s.Name).Set(s.InFlight)
	}
}
